var c = 1 //A variavel "c" já vai começar com 1 em seu contador
while(c <= 6){ //Condição de uma estrutura de repetição: Enquanto c(que é nossa vaiavel) for menor ou igual a 6, faça.
    console.log(`Passo ${c}`) //Isso vai aparecer 6 vezes escrito na tela.
    c++ //c = c + 1. Aqui estamos falando que, toda vez que o laço de repetição voltar para a condição, adicionamos mais 1 a variavel.
}